    private func loadImage() {
        guard let url = URL(string: resource.coverImageUrl) else {
            print("‚ùå Êó†ÊïàÁöÑÂ∞ÅÈù¢ÂõæÁâáURL: \(resource.coverImageUrl)")
            DispatchQueue.main.async {
                self.isLoading = false
                self.loadError = true
            }
            return
        }
        
        print("üì∏ ÂºÄÂßãÂä†ËΩΩÂ∞ÅÈù¢ÂõæÁâá: \(url)")
        URLSession.shared.dataTask(with: url) { data, response, error in
            if let error = error {
                print("‚ùå Âä†ËΩΩÂ∞ÅÈù¢ÂõæÁâáÂ§±Ë¥•: \(error.localizedDescription)")
                DispatchQueue.main.async {
                    self.isLoading = false
                    self.loadError = true
                }
                return
            }
            
            if let httpResponse = response as? HTTPURLResponse {
                print("üì° Â∞ÅÈù¢ÂõæÁâáÂìçÂ∫îÁä∂ÊÄÅÁ†Å: \(httpResponse.statusCode)")
            }
            
            if let data = data, let image = UIImage(data: data) {
                print("‚úÖ ÊàêÂäüÂä†ËΩΩÂ∞ÅÈù¢ÂõæÁâá")
                DispatchQueue.main.async {
                    self.image = image
                    self.isLoading = false
                }
            } else {
                print("‚ùå Êó†Ê≥ï‰ªéÊï∞ÊçÆÂàõÂª∫ÂõæÁâá")
                DispatchQueue.main.async {
                    self.isLoading = false
                    self.loadError = true
                }
            }
        }.resume()
    }
}

