                    }
                }
                .padding()
                .background(Constants.Colors.resourceRowBackground)
            }
            .cornerRadius(Constants.Layout.cornerRadius)
            .shadow(radius: 5)
        }
        .buttonStyle(PlainButtonStyle())
        .onAppear {
            loadImage()
        }
        .confirmationDialog("ÁÆ°ÁêÜËµÑÊ∫ê", isPresented: $showActionSheet) {
            Button("Âà†Èô§ËµÑÊ∫ê", role: .destructive) {
                showDeleteAlert = true
            }
            
            Button("Ê∑ªÂä†Ê†áÁ≠æ") {
                // TODO: ÂÆûÁé∞Ê∑ªÂä†Ê†áÁ≠æÂäüËÉΩ
            }
            
            Button("ËÆæÁΩÆ Rank ÂÄº") {
                // TODO: ÂÆûÁé∞ËÆæÁΩÆ Rank ÂÄºÂäüËÉΩ
            }
            
            Button("ÂèñÊ∂à", role: .cancel) {}
        } message: {
            Text(resource.name)
        }
        .alert("Á°ÆËÆ§Âà†Èô§", isPresented: $showDeleteAlert) {
            Button("ÂèñÊ∂à", role: .cancel) {}
            Button("Âà†Èô§", role: .destructive) {
